class Student {
    private int id;
    private String name;

    // Creating a constructor of the class named Student
    public Student(int id, String name) {
        this.id = id;
        this.name = name;
    }

    // Creating a getter method to get the id of the student
    public int getId() {
        return id;
    }

    // Creating the getter method to get the name of the student
    public String getName() {
        return name;
    }
}
